                        <br> It all started in a very remote village, in the district Jaunpur, in the state of Uttar Pradesh back in the year 2003. In a fairly backward setting, with a considerably large population base of economically weaker sections and communities viz. SC/ST, OBC and various other minority groups, it was with the effort of a retired government servant committed with a vision to transform the area, that he took upon himself the initiative to teach the girl-child. Shri SP Shukla, knew that education was a golden compass – it pointed the way out of poverty, but there could be no quick tricks. In 2003, since there was no school nearby which offered education above 8th standard, Mr Shukla constituted an NGO Malti Seva Samiti and started working towards setting up a school offering education till Intermediate. This soon turned out to be an unspeakable boon especially for girls of this area who were deprived of education due to unavailability of affordable options. Besides, due to poverty, malnutrition and poor living standards, their guardians could not arrange higher education for their children. Malti Seva Samiti, the NGO registered under Society Registration Act 21, 1860 in 2002 and Income Tax department, decided to open a school with a focus on girl education in a remote area in Jaunpur district by the name of “Rashtriya Uchchatar Madhyamik Vidyalaya”, Himmatnagar, Dharikpur, Jaunpur, U.P.
